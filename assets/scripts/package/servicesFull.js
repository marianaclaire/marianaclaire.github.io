var currentLang = "ENG";

function parseService(){
	var url = window.location.href;
	var args = url.split('#');
	if (args.length > 1){
		if(servicesFull[args[1]]){
			return args[1];
		}
		if(servicesFull[validLangsServices.FREN[args[1]]]){
			currentLang = "FREN";
			return validLangsServices.FREN[args[1]];
		}
		if(servicesFull[validLangsServices.ESP[args[1]]]){
			currentLang = "ESP";
			return validLangsServices.ESP[args[1]];
		}
	}
	// redirectd
	window.location.href = "../index.html#services";
}

var service = parseService();

// angular
var app = angular.module('app', []);

angular.module('app')
    .filter('to_trusted', ['$sce', function($sce){
        return function(text) {
            return $sce.trustAsHtml(text);
        };
    }]);

function viewCtrl($scope) {
	$scope.languageOpt = currentLang;
	$scope.service = servicesFull[service];
	$scope.services = services;

	$scope.changeService = function(slug) {
		$('.splash-bg').hide();
		window.location.href = "../services/index.html#"+slug;
		window.location.reload();
	}

	$scope.refresh = function() {
		window.setTimeout(function(){
			initBacklight({
				type: 'text'
			});
		}, 500);
	};
}

function setBg(){
	$div = $('.splash-bg');
	$div.hide();
	$div.css('background-image','url("../assets/images/services/'+servicesFull[service].image+'")');
	$div.fadeIn('fast');
}

(function(){
	setBg();
	//language
	$('.lang-'+currentLang).css({
		'display': 'inline-block'
	});
}());